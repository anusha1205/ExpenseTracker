            Class.forName("com.mysql.cj.jdbc.Driver");
            String db = "jdbc:mysql://localhost:3306/expensetracker";
            String dbUsername = "root";
            String dbPassword = "";

            Connection con = DriverManager.getConnection(db, dbUsername, dbPassword);

